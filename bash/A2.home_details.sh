#!/bin/bash

. ./00.variables.sh
SESSION_NAME=$(cat ${WORKDIR}/${SESSION_FILENAME})
BEARER_TOKEN=$(cat ${WORKDIR}/${SESSION_DIR_PREFIX}${SESSION_NAME}/${OAUTH_ACCESS_TOKEN})

TADO_HOME_ID=$(cat ${WORKDIR}/${SESSION_DIR_PREFIX}${SESSION_NAME}/${TADO_DATA_HOME_ID})

echo "invoking uri >${TADO_API_HOME_DETAILS_BASE_URI}/${TADO_HOME_ID}<"
curl -s "${TADO_API_HOME_DETAILS_BASE_URI}/${TADO_HOME_ID}" \
     -H "Authorization: Bearer ${BEARER_TOKEN}" \
     -o ${WORKDIR}/${SESSION_DIR_PREFIX}${SESSION_NAME}/${TADO_API_HOME_DETAILS_JSON_RESULT}

